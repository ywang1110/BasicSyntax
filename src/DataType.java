public class DataType {
    public static void main(String[] args) {
        // ************************ Primitive Type *****************
        // Integer
        byte num1 = 20;
        short num2 = 20;
        int num3 = 30;   // int is the most common datatype
        long num4 = 40L; // end with L

        // Float
        float num5 = 50.1F;  // end with F
        double num6 = 3.14159267;

        // Character
        char name = 'A';

        // Boolean
        boolean flag = true;

        // ********* Non-Primitive Type ************************
        // String
        String name1 = "Amy";
    }
}
