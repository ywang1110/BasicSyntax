public class IntegerExpansion {
    public static void main(String[] args) {
        // 二进制(0b)          十进制               八进制(0)           十六进制(0x)
        int i = 10;
        int i2 = 010;  //    八进制(0)
        int i3 = 0x10;  //    十六进制(0x)  0-9 A-F

        System.out.println(i);
        System.out.println(i2);
        System.out.println(i3);
    }
}
